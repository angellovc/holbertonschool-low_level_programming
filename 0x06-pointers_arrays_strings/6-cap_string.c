#include "holberton.h"
#include <stdio.h>
/**
 **cap_string - lower to upper
 *@s: string
 *
 *Return: pointer
 */
char *cap_string(char *s)
{
	int i = 0;

	while (s[i] != '\0')
	{
		if ((s[i] == 32) && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == ',') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == ';') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '.') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '!') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '?') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '"') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '(') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == ')') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '{') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '}') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == '\n') && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		if ((s[i] == 9) && (s[i + 1] >= 97 && s[i + 1] <= 122))
			s[i + 1] = s[i + 1] - 32;
		i++;
	}
	return (s);
}
